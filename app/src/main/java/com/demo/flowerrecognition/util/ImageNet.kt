package com.demo.flowerrecognition.util

enum class CategoryType {
    CAT1000, CAT110, CATFLOWER404
}

object ImageNet {

    fun findMax(result: FloatArray?, type: CategoryType): Pair<String?, Int> {
        if (result == null) {
            return Pair("", -1)
        }
        var max = 0f
        var maxIndex = 0
        for (i in result.indices) {
            if (result[i] > max) {
                max = result[i]
                maxIndex = i
            }
        }
        max = ((max * 1000).toInt()/10.0).toFloat()
        return when (type) {
            CategoryType.CAT1000 -> Pair(
                "${category1000[maxIndex]}(${max}%)",
                maxIndex
            )
            CategoryType.CAT110 -> Pair(
                "${category110[maxIndex]}(${max}%)",
                maxIndex
            )
            CategoryType.CATFLOWER404 -> Pair(
                "${categoryFlower404[maxIndex]}(${max}%)",
                maxIndex
            )
        }
//        return Pair("${category1000[maxIndex]} : ${max}（${maxIndex})", maxIndex)
    }

